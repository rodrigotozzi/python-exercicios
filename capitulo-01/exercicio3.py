#Faça um programa que recebe três valores e apresente a soma dos quadrados dos valores lidos

numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))
numero3 = int(input("Digite o terceiro número: "))

print("Os número digitados foram:", numero1, numero2, numero3)
print("A soma do quadrado dos três número é:", (numero1**2)+(numero2**2)+(numero3**2))
